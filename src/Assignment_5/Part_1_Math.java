package Assignment_5;

public class Part_1_Math {

/*Calling static method: Static method can be called by using className.methodName()or
  simply methodName() if they are being called in the same class in which they exist.
* Calling non static method: in order to call this methods we need to create an object of the class
  in which they exist and call them using this object as objectName.methodName()*/
//To allow operation of decimals all the methods return type and arguments are take as double.

	public static double add(double a, double b) {//static method with return type double and 2 double arguments
		return a+b;
	}
	public static double subtract(double a, double b) {//static method with return type double and 2 double arguments
		return a-b;
	}
	public static double multiply(double a, double b) {//non static method with return type double and 2 double arguments
		return a+b;
	}
	public static double divide(double a, double b) {//non static method with return type double and 2 double arguments
		return a+b;
	}
    






}
